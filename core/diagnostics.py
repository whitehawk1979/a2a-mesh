    async def handle_diagnostic_message(self, message: dict):
        """Handle incoming diagnostic message from the mesh."""
        msg_type = message.get("type", "")
        source = message.get("source", "unknown")
        data = message.get("data", {})
        
        if msg_type == "diagnostic_report":
            report = DiagnosticReport.from_dict(data)
            # Deduplicate by report_id — same report may arrive via P2P and PG NOTIFY
            if any(r.report_id == report.report_id for r in self._reports):
                log.debug(f"📊 Duplicate diagnostic report from {source}: {report.report_id} — skipping")
                return
            self._store_report(report)
            log.info(f"📊 Received diagnostic report from {source}: {report.report_id}")
        
        elif msg_type == "config_suggestion":
            suggestion = ConfigSuggestion.from_dict(data)
            # Deduplicate by suggestion_id
            if any(s.suggestion_id == suggestion.suggestion_id for s in self._suggestions):
                log.debug(f"💡 Duplicate config suggestion from {source}: {suggestion.title} — skipping")
                return
            self._suggestions.append(suggestion)
            if len(self._suggestions) > self.config.max_reports_stored:
                self._suggestions = self._suggestions[-self.config.max_reports_stored:]
            log.info(f"💡 Received config suggestion from {source}: {suggestion.title}")
    
    def record_error(self, error_type: str):
        """Record an error occurrence for pattern analysis."""
        self._error_counts[error_type] = self._error_counts.get(error_type, 0) + 1
    
    def get_reports(self, limit: int = 20, severity: Optional[str] = None) -> List[DiagnosticReport]:
        """Get stored diagnostic reports, optionally filtered by severity."""
        reports = self._reports
        if severity:
            reports = [r for r in reports if r.severity == severity]
        return reports[-limit:]
    
    def get_suggestions(self, limit: int = 20, category: Optional[str] = None) -> List[ConfigSuggestion]:
        """Get stored config suggestions, optionally filtered by category."""
        suggestions = self._suggestions
        if category:
            suggestions = [s for s in suggestions if s.category == category]
        return suggestions[-limit:]
    
    def update_suggestion_status(self, suggestion_id: str, new_status: str) -> Optional[ConfigSuggestion]:
        """Update the status of a suggestion (pending/accepted/rejected/implemented)."""
        valid = {"pending", "accepted", "rejected", "implemented"}
        if new_status not in valid:
            return None
        for s in self._suggestions:
            if s.suggestion_id == suggestion_id:
                old_status = s.status
                s.status = new_status
                log.info(f"📋 Suggestion {suggestion_id}: {old_status} → {new_status}")
                return s
        return None
    
